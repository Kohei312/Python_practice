## 数値型(Int,float) ⇆　文字列型

## 数値型 → 文字列型 : str()で、数値型をラップする
# int_num = 10 
# float_num = 22.15

# print("num = " + str(int_num))
# print("num = " + str(float_num))

## 文字列　→　数値型 : 整数ではint()で、浮動小数点型ではfloat()で、それぞれ文字列をラップする

# msg = "10"
# str_int = int(msg)
# str_fl = float(msg)

# print("value = {}, type = {}".format(str_int,type(str_int)))
# print("value = {}, type = {}".format(str_fl,type(str_fl)))
